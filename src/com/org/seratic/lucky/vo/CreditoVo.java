package com.org.seratic.lucky.vo;

public class CreditoVo {

	private String cod_reporte;
	private String cod_credito;
	private String nom_credito;

	
	
	public void setCod_reporte(String cod_reporte) {
		this.cod_reporte = cod_reporte;
	}

	public String getCod_reporte() {
		return cod_reporte;
	}

	public void setCod_credito(String cod_credito) {
		this.cod_credito = cod_credito;
	}

	public String getCod_credito() {
		return cod_credito;
	}

	public void setNom_credito(String nom_credito) {
		this.nom_credito = nom_credito;
	}

	public String getNom_credito() {
		return nom_credito;
	}

	
}
