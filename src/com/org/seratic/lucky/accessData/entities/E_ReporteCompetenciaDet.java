package com.org.seratic.lucky.accessData.entities;

public class E_ReporteCompetenciaDet {

	private int id;
	private int id_rep_competencia;
	private String cod_material;
	private String desc_material;
	private boolean selected;
	
	
	public E_ReporteCompetenciaDet() {
	}
	
	public E_ReporteCompetenciaDet(int id_rep_competencia, String cod_material) {
		this.setId_rep_competencia(id_rep_competencia);
		this.cod_material = cod_material;
	}
	

	public void setCod_material(String cod_material) {
		this.cod_material = cod_material;
	}

	public String getCod_material() {
		return cod_material;
	}

	
	public void setId_rep_competencia(int id_rep_competencia) {
		this.id_rep_competencia = id_rep_competencia;
	}


	public int getId_rep_competencia() {
		return id_rep_competencia;
	}


	public void setId(int id) {
		this.id = id;
	}


	public int getId() {
		return id;
	}

	public void setDesc_material(String desc_material) {
		this.desc_material = desc_material;
	}

	public String getDesc_material() {
		return desc_material;
	}

	public void setSelected(boolean selected) {
		this.selected = selected;
	}

	public boolean isSelected() {
		return selected;
	}

}
