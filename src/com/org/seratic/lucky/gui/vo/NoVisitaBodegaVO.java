package com.org.seratic.lucky.gui.vo;

import com.org.seratic.lucky.accessData.entities.E_MotivoNoVisita;

public class NoVisitaBodegaVO  extends E_MotivoNoVisita {

	private boolean checked;

	public NoVisitaBodegaVO() {
		super();
	}

	public NoVisitaBodegaVO(E_MotivoNoVisita d) {
		super();
		this.setId(d.getId());
		this.setIdNoVisita(d.getIdNoVisita());
		this.setGrupo(d.getGrupo());
		this.setDescripcion(d.getDescripcion());	
	}
	
	public boolean isChecked() {
		return checked;
	}

	public void setChecked(boolean checked) {
		this.checked = checked;
	}
}
