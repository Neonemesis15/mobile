package com.org.seratic.lucky.model;

public class E_UsuarioRequest {
	
	private String u;
	private String p;
	
	public E_UsuarioRequest(){}
	/**
	 * 	
	 * @param u Nombre de usuario
	 * @param p Contraseña
	 */
	public E_UsuarioRequest(String u, String p) {
		super();
		this.u = u;
		this.p = p;
	}
	public String getU() {
		return u;
	}
	public void setU(String u) {
		this.u = u;
	}
	public String getP() {
		return p;
	}
	public void setP(String p) {
		this.p = p;
	}


}
